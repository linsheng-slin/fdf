#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char		*a;
	int					i;

	i = 0;
	a = b;
	while (len-- > 0)
	{
		a[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
